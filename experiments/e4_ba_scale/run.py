"""
E4 — Barabási-Albert Scale Experiment

Evaluates revocation failure rates on realistic-scale multi-parent DAGs
generated by the Barabási-Albert preferential attachment model.

Graph model:
  - BA(n, m): n nodes, each new node attaches to m existing nodes
    (preferential attachment → hub structure, high multi-parent rate)
  - Directed: older nodes → newer nodes (delegation flows downward)
  - Deployers: 80% d1, 20% d2 (cross-domain revocation scenarios)

Metrics vs T_intent (disjunctive reachability semantics):
  - OverRev: tree_cascade, L0
  - UnderRev: deployer_scope
  - Prevalence: % edges where error > 0

Design: n ∈ {100, 500, 2000}, m ∈ {2, 4},
        50 graphs × 50 edges per cell.
"""

import sys
import time
from pathlib import Path

import networkx as nx
import numpy as np

sys.path.insert(0, str(Path(__file__).parents[2] / "src"))

# ── Graph generation ──────────────────────────────────────────────────────────

def make_ba_dag(n: int, m: int, d2_ratio: float = 0.2, seed: int = 0):
    """
    Build a directed acyclic delegation DAG via BA preferential attachment.

    Returns: (DiGraph G, deployer dict, roots set)
    """
    rng = np.random.RandomState(seed)
    G_u = nx.barabasi_albert_graph(n, m, seed=seed)
    G = nx.DiGraph()
    G.add_nodes_from(range(n))
    for u, v in G_u.edges():
        # Direction: older (lower index) → newer (higher index)
        if u < v:
            G.add_edge(u, v)
        else:
            G.add_edge(v, u)
    deployer = {node: "d2" if rng.random() < d2_ratio else "d1"
                for node in G.nodes()}
    roots = frozenset(node for node in G.nodes() if G.in_degree(node) == 0)
    if not roots:
        roots = frozenset({0})
    return G, deployer, roots


# ── Reachability helpers ──────────────────────────────────────────────────────

def reach_from(G: nx.DiGraph, roots) -> set:
    visited = set(roots)
    for r in roots:
        visited |= nx.descendants(G, r)
    return visited


def t2(G: nx.DiGraph, edge, roots, precomp_reach=None) -> set:
    """T_intent(e, G) = reach(G) - reach(G\{e})."""
    full = precomp_reach if precomp_reach is not None else reach_from(G, roots)
    G.remove_edge(*edge)
    without = reach_from(G, roots)
    G.add_edge(*edge)
    return full - without


def tree_cascade(G: nx.DiGraph, edge) -> set:
    child = edge[1]
    return {child} | nx.descendants(G, child)


def deployer_scope(G: nx.DiGraph, edge, deployer: dict, d: str) -> set:
    return {v for v in tree_cascade(G, edge) if deployer[v] == d}


def l0(G: nx.DiGraph, deployer: dict, d: str, roots) -> set:
    d_nodes = {v for v in G.nodes() if deployer[v] == d}
    desc = set()
    for v in d_nodes:
        desc |= nx.descendants(G, v)
    return d_nodes | desc


# ── Single trial ──────────────────────────────────────────────────────────────

def run_trial(n: int, m: int, n_graphs: int = 50, n_edges: int = 50):
    rng_global = np.random.RandomState(n * 100 + m)

    results = {
        "overrev_tree":   [],
        "overrev_l0":     [],
        "underrev_scope": [],
        "t2_size":        [],
        "multiparent_rate": [],
        "n_valid_edges":  [],
    }

    for g_idx in range(n_graphs):
        seed = int(rng_global.randint(0, 2**30))
        G, deployer, roots = make_ba_dag(n, m, seed=seed)

        # Multi-parent rate (non-root nodes with in_degree > 1)
        non_roots = [v for v in G.nodes() if v not in roots]
        if non_roots:
            mp_rate = sum(1 for v in non_roots if G.in_degree(v) > 1) / len(non_roots)
        else:
            mp_rate = 0.0
        results["multiparent_rate"].append(mp_rate)

        # Precompute full reachability
        full_reach = reach_from(G, roots)

        # Candidate edges: non-root parents (excludes trivial edges from roots)
        root_set = set(roots)
        candidate_edges = [e for e in G.edges() if e[0] not in root_set]
        if not candidate_edges:
            candidate_edges = list(G.edges())
        if not candidate_edges:
            continue

        rng_local = np.random.RandomState(seed + 1)
        chosen = [candidate_edges[i] for i in
                  rng_local.choice(len(candidate_edges),
                                   size=min(n_edges, len(candidate_edges)),
                                   replace=False)]
        results["n_valid_edges"].append(len(chosen))

        for edge in chosen:
            intended = t2(G, edge, roots, precomp_reach=full_reach)
            casc = tree_cascade(G, edge)
            scope = deployer_scope(G, edge, deployer, "d1")
            l0_set = l0(G, deployer, deployer[edge[0]], roots)

            results["overrev_tree"].append(len(casc - intended))
            results["overrev_l0"].append(len(l0_set - intended))
            results["underrev_scope"].append(len(intended - scope))
            results["t2_size"].append(len(intended))

    return results


# ── Summary statistics ────────────────────────────────────────────────────────

def summarise(results: dict, n: int):
    ot = np.array(results["overrev_tree"])
    ol = np.array(results["overrev_l0"])
    us = np.array(results["underrev_scope"])
    t2s = np.array(results["t2_size"])
    mp = np.array(results["multiparent_rate"])

    return {
        "n": n,
        "mp_rate_pct": 100 * mp.mean(),
        "t2_mean": t2s.mean(),
        "t2_mean_pct": 100 * t2s.mean() / n,
        # tree cascade
        "tree_prev_pct": 100 * (ot > 0).mean(),
        "tree_overrev_mean": ot.mean(),
        "tree_overrev_pct": 100 * ot.mean() / n,
        # L0
        "l0_prev_pct": 100 * (ol > 0).mean(),
        "l0_overrev_mean": ol.mean(),
        "l0_overrev_pct": 100 * ol.mean() / n,
        # deployer scope
        "scope_prev_pct": 100 * (us > 0).mean(),
        "scope_underrev_mean": us.mean(),
        "scope_underrev_pct": 100 * us.mean() / n,
    }


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    configs = [
        (100,  2, 50, 50),
        (100,  4, 50, 50),
        (500,  2, 50, 50),
        (500,  4, 50, 50),
        (2000, 2, 30, 30),
        (2000, 4, 30, 30),
    ]

    all_summaries = []

    print(f"\n{'n':>6} {'m':>3} {'MP%':>7} {'T2%':>7} "
          f"{'Casc Prev%':>11} {'Casc OR%':>10} "
          f"{'L0 Prev%':>9} {'L0 OR%':>8} "
          f"{'Scope UR%':>10}")
    print("-" * 85)

    for n, m, n_graphs, n_edges in configs:
        t0 = time.time()
        print(f"  Running n={n}, m={m}, {n_graphs}g×{n_edges}e ...", end="", flush=True)
        results = run_trial(n, m, n_graphs=n_graphs, n_edges=n_edges)
        s = summarise(results, n)
        s["m"] = m
        all_summaries.append(s)
        elapsed = time.time() - t0
        print(f" done ({elapsed:.1f}s)")
        print(f"  {n:>6} {m:>3} {s['mp_rate_pct']:>6.1f}% {s['t2_mean_pct']:>6.2f}% "
              f"  {s['tree_prev_pct']:>8.1f}%   {s['tree_overrev_pct']:>6.2f}%  "
              f"  {s['l0_prev_pct']:>6.1f}%  {s['l0_overrev_pct']:>6.2f}%  "
              f"  {s['scope_prev_pct']:>7.1f}%")

    print()
    print("Key findings:")
    for s in all_summaries:
        print(f"  n={s['n']:>4}, m={s['m']}: "
              f"multi-parent={s['mp_rate_pct']:.1f}%, "
              f"tree-cascade fails {s['tree_prev_pct']:.1f}% of revocations "
              f"(mean OverRev={s['tree_overrev_mean']:.1f} nodes, "
              f"{s['tree_overrev_pct']:.2f}% of graph)")

    return all_summaries


if __name__ == "__main__":
    main()
